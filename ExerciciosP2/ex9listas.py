# 25. Soma de todos os elementos de uma lista
def soma_lista(lista):
    return sum(lista)

# 26. Remover elementos duplicados de uma lista
def remover_duplicados(lista):
    return list(set(lista))

# 27. Elementos comuns entre duas listas
def elementos_comuns(lista1, lista2):
    return list(set(lista1) & set(lista2))